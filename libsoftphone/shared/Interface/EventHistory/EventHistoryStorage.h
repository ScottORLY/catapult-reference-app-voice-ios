        //*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-
        {
            EventStream **s = EventStream::sCache.find(streamKey);
            if (s == nullptr)
                return nullptr;
            return *s;
        }

