        //*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-
        {
            for (int i = 0; i < Event::sCache.size(); ++i)
                callback(*Event::sCache.at(i).second);
        }

