        //*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-*-
        {
            for (int i = 0; i < EventStream::sCache.size(); ++i)
                callback(*EventStream::sCache.at(i).second);
        }

